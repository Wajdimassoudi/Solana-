const timeAgo = (seconds: number, lang: 'en' | 'es' | 'fr' | 'ar'): string => {
  if (seconds < 5) return { en: 'just now', es: 'ahora mismo', fr: 'à l\'instant', ar: 'الآن' }[lang];
  if (seconds < 60) return { en: `${seconds}s ago`, es: `hace ${seconds}s`, fr: `il y a ${seconds}s`, ar: `منذ ${seconds} ثانية` }[lang];
  if (seconds < 3600) {
    const minutes = Math.floor(seconds / 60);
    return { en: `${minutes}m ago`, es: `hace ${minutes}m`, fr: `il y a ${minutes}m`, ar: `منذ ${minutes} دقيقة` }[lang];
  }
  const hours = Math.floor(seconds / 3600);
  return { en: `${hours}h ago`, es: `hace ${hours}h`, fr: `il y a ${hours}h`, ar: `منذ ${hours} ساعة` }[lang];
};

